export function Icon({ name, size = 24, ...props }) {
  let icon;
  
  // Handle nested icons like chevron.down
  if (name.includes('.')) {
    const [category, variant] = name.split('.');
    icon = icons[category][variant];
  } else {
    icon = icons[name];
  }
  
  if (!icon) {
    console.warn(`Icon '${name}' not found`);
    return null;
  }
  
  // Clone the icon with new props
  return React.cloneElement(icon, {
    width: size,
    height: size,
    ...props
  });
}

export default Icon;
